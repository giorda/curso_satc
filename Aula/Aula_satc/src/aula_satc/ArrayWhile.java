public class ArrayWhile {
        public static void main(String[] args){
        String days[] = {"Monday","Tuesday","Wednesday","Tursday", "Friday",
            "Saturday","Sunday"};
        Integer i = 0;
        while (i < days.length){
            System.out.println(days[i]);
            i++;
        }
}
    
}
